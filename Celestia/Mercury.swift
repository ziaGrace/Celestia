import UIKit
import SceneKit

class Mercury: Planet {

    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
    }
    
    init(geometry: SCNGeometry?){
        super.init()
        self.geometry = geometry
        name = "Mercury"
        information = """
Surface area: 7.48×107 km²
Volume: 6.083×1010 km³
Mass: 3.3011×1023 kg
Mercury is the smallest and innermost planet in the Solar System. Its orbital period around the Sun of 87.97 days is the shortest of all the planets in the Solar System. It is named after the Roman deity Mercury, the messenger of the gods.
"""
        celestialDiffuse = UIImage(named: "mercurymap")
        celestialNormal = UIImage(named: "mercurybump")
       
        
        
        let rotationAction = SCNAction.rotate(by: 2.0, around: SCNVector3(0, 1, 0), duration: 10)
        
        let rotationForeverAction = SCNAction.repeatForever(rotationAction)
        
        rotationForce = rotationForeverAction
        
        
    
    }
    
}
